i = 1
while i <= 100:
    print(f"今天是第{i}天，准备表白···")

    # 内层循环的控制变量
    j = 1
    while j <= 10:
        print(f"送给小美第{j}朵花")
        j += 1

    print("小美，我喜欢你。")
    i += 1

print(f"坚持到第{i - 1}天，表白成功。")